    public static void drawRadius(final double xIn, final double yIn, final double zIn, final float partialTicks, final double rad, Color colorIn, int pointsIn, boolean outline) {

        final float lineWidth = 2.0F;
        final float outlineWidth = 4.0F;

        float points = (float) pointsIn;
        Color color = outline ? Color.black : colorIn;
        float width = outline ? outlineWidth : lineWidth;

        GlStateManager.enableDepth();
        for (int ii = 0; ii < (outline ? 2 : 1); ii++) {
            GL11.glPushMatrix();
            GL11.glDisable(GL11.GL_TEXTURE_2D);
            GL11.glEnable(GL11.GL_LINE_SMOOTH);
            GL11.glEnable(GL11.GL_POLYGON_SMOOTH);
            GL11.glEnable(GL11.GL_POINT_SMOOTH);
            GL11.glEnable(GL11.GL_BLEND);
            GL11.glBlendFunc(GL11.GL_SRC_ALPHA, GL11.GL_ONE_MINUS_SRC_ALPHA);
            GL11.glHint(GL11.GL_LINE_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glHint(GL11.GL_POLYGON_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glHint(GL11.GL_POINT_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glDisable(GL11.GL_DEPTH_TEST);
            GL11.glLineWidth(width);
            GL11.glBegin(GL11.GL_LINE_STRIP);
            final double x = xIn * partialTicks - mc.getRenderManager().viewerPosX;
            final double y = yIn * partialTicks - mc.getRenderManager().viewerPosY;
            final double z = zIn * partialTicks - mc.getRenderManager().viewerPosZ;
            final double pix2 = Math.PI * 2.0;
            for (int i = 0; i <= points; ++i) {
                RenderUtil.glColor(color);
                GL11.glVertex3d(x + rad * Math.cos(i * pix2 / points), y, z + rad * Math.sin(i * pix2 / points));
            }
            GL11.glEnd();
            GL11.glDepthMask(true);
            GL11.glEnable(GL11.GL_DEPTH_TEST);
            GL11.glDisable(GL11.GL_LINE_SMOOTH);
            GL11.glDisable(GL11.GL_POLYGON_SMOOTH);
            GL11.glEnable(GL11.GL_POINT_SMOOTH);
            GL11.glEnable(GL11.GL_TEXTURE_2D);
            GL11.glPopMatrix();
            RenderUtil.glColor(Color.WHITE);

            color = colorIn;
            width = lineWidth;
        }
    }

    public static void drawRadius(final Entity entity, final float partialTicks, final double rad, Color colorIn, int pointsIn, boolean outline) {
        if (entity == null) return;

//        final double x = entity.lastTickPosX + (entity.posX - entity.lastTickPosX) * partialTicks - mc.getRenderManager().viewerPosX;
//        final double y = entity.lastTickPosY + (entity.posY - entity.lastTickPosY) * partialTicks - mc.getRenderManager().viewerPosY;
//        final double z = entity.lastTickPosZ + (entity.posZ - entity.lastTickPosZ) * partialTicks - mc.getRenderManager().viewerPosZ;
//
//        drawRadius(x, y, z, partialTicks, rad, colorIn, pointsIn, outline);

        final float lineWidth = 2.0F;
        final float outlineWidth = 4.0F;

        float points = (float) pointsIn;
        Color color = outline ? Color.black : colorIn;
        float width = outline ? outlineWidth : lineWidth;

        GlStateManager.enableDepth();
        for (int ii = 0; ii < (outline ? 2 : 1); ii++) {
            GL11.glPushMatrix();
            GL11.glDisable(GL11.GL_TEXTURE_2D);
            GL11.glEnable(GL11.GL_LINE_SMOOTH);
            GL11.glEnable(GL11.GL_POLYGON_SMOOTH);
            GL11.glEnable(GL11.GL_POINT_SMOOTH);
            GL11.glEnable(GL11.GL_BLEND);
            GL11.glBlendFunc(GL11.GL_SRC_ALPHA, GL11.GL_ONE_MINUS_SRC_ALPHA);
            GL11.glHint(GL11.GL_LINE_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glHint(GL11.GL_POLYGON_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glHint(GL11.GL_POINT_SMOOTH_HINT, GL11.GL_NICEST);
            GL11.glDisable(GL11.GL_DEPTH_TEST);
            GL11.glLineWidth(width);
            GL11.glBegin(GL11.GL_LINE_STRIP);
            final double x = entity.lastTickPosX + (entity.posX - entity.lastTickPosX) * partialTicks - mc.getRenderManager().viewerPosX;
            final double y = entity.lastTickPosY + (entity.posY - entity.lastTickPosY) * partialTicks - mc.getRenderManager().viewerPosY;
            final double z = entity.lastTickPosZ + (entity.posZ - entity.lastTickPosZ) * partialTicks - mc.getRenderManager().viewerPosZ;
            final double pix2 = Math.PI * 2.0;
            for (int i = 0; i <= points; ++i) {
                RenderUtil.glColor(color);
                GL11.glVertex3d(x + rad * Math.cos(i * pix2 / points), y, z + rad * Math.sin(i * pix2 / points));
            }
            GL11.glEnd();
            GL11.glDepthMask(true);
            GL11.glEnable(GL11.GL_DEPTH_TEST);
            GL11.glDisable(GL11.GL_LINE_SMOOTH);
            GL11.glDisable(GL11.GL_POLYGON_SMOOTH);
            GL11.glEnable(GL11.GL_POINT_SMOOTH);
            GL11.glEnable(GL11.GL_TEXTURE_2D);
            GL11.glPopMatrix();
            RenderUtil.glColor(Color.WHITE);

            color = colorIn;
            width = lineWidth;
        }
    }

    public static AxisAlignedBB toRender(AxisAlignedBB axisAlignedBB) {
        return new AxisAlignedBB(
                axisAlignedBB.minX - mc.getRenderManager().viewerPosX,
                axisAlignedBB.minY - mc.getRenderManager().viewerPosY,
                axisAlignedBB.minZ - mc.getRenderManager().viewerPosZ,
                axisAlignedBB.maxX - mc.getRenderManager().viewerPosX,
                axisAlignedBB.maxY - mc.getRenderManager().viewerPosY,
                axisAlignedBB.maxZ - mc.getRenderManager().viewerPosZ
        );
    }

    private static final IntBuffer viewport = GLAllocation.createDirectIntBuffer(16);
    private static final FloatBuffer modelView = GLAllocation.createDirectFloatBuffer(16);
    private static final FloatBuffer projection = GLAllocation.createDirectFloatBuffer(16);
    private static final FloatBuffer vector = GLAllocation.createDirectFloatBuffer(4);

    public static javax.vecmath.Vector3d project2D(int scaleFactor, double x, double y, double z) {
        GL11.glGetFloat(GL11.GL_MODELVIEW_MATRIX, modelView);
        GL11.glGetFloat(GL11.GL_PROJECTION_MATRIX, projection);
        GL11.glGetInteger(GL11.GL_VIEWPORT, viewport);
        return GLU.gluProject((float) x, (float) y, (float) z, modelView, projection, viewport, vector) ? new Vector3d(vector.get(0) / (float) scaleFactor, ((float) Display.getHeight() - vector.get(1)) / (float) scaleFactor, vector.get(2)) : null;
    }

