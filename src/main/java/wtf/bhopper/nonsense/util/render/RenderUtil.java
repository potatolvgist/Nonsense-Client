    public static void drawTracer(final BlockPos blockPos, final Color color, float lineWidth) {
        BlockPos nearestToPlayer = blockPos;
        float dist = MathHelper.sqrt_double(mc.thePlayer.getDistanceSqToCenter(blockPos));
        for (Vec3i vec : new Vec3i[]{
                new Vec3i(1, 0, 0), new Vec3i(0, 1, 0), new Vec3i(0, 0, 1),
                new Vec3i(1, 1, 0), new Vec3i(1, 0, 1), new Vec3i(0, 1, 1),
                new Vec3i(1, 1, 1)
        }) {
            BlockPos testPos = blockPos.add(vec);
            float testDist = MathHelper.sqrt_double(mc.thePlayer.getDistanceSqToCenter(testPos));
            if (testDist < dist) {
                nearestToPlayer = testPos;
                dist = testDist;
            }
        }
        drawTracer(nearestToPlayer.getX(), nearestToPlayer.getY(), nearestToPlayer.getZ(), color, lineWidth);
    }

