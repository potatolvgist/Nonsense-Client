package wtf.bhopper.nonsense.util.minecraft.player;

public class Rotation {

    public float yaw;
    public float pitch;

    public Rotation() {
        this.yaw = 0.0F;
        this.pitch = 0.0F;
    }

    public Rotation(float yaw, float pitch) {
        this.yaw = yaw;
        this.pitch = pitch;
    }


}
